export function getContentT(locale: ContentLocale) {
  return contentMessages[locale] ?? contentMessages.en;
}

const BROWSER_LOCALE_MAP: Record<string, ContentLocale> = {
  ro: "ro", en: "en", de: "de", it: "it", es: "es", fr: "fr", pt: "pt", nl: "nl", pl: "pl", tr: "tr",
};

/** Detect locale from browser (e.g. navigator.language). Use only on client. */
export function getBrowserLocale(): ContentLocale {
  if (typeof navigator === "undefined") return "en";
  const lang = (navigator.language || (navigator as { userLanguage?: string }).userLanguage || "en").slice(0, 2).toLowerCase();
  return BROWSER_LOCALE_MAP[lang] ?? "en";
}
